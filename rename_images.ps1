$sourceDir = "C:\Users\Juanjo\cofradia\Cofradia\imagenes_provicionales"

foreach ($oldName in $renames.Keys) {
    $newName = $renames[$oldName]
    $oldPath = Join-Path $sourceDir $oldName
    $newPath = Join-Path $sourceDir $newName
    
    if (Test-Path $oldPath) {
        Rename-Item -Path $oldPath -NewName $newName -Force
        Write-Host "Renombrado: $oldName -> $newName"
    } else {
        Write-Host "NO ENCONTRADO: $oldName" -ForegroundColor Red
    }
}

Write-Host "`nProceso completado. Verificando..." -ForegroundColor Green

# Listar archivos que no fueron renombrados
$remaining = Get-ChildItem -Path $sourceDir | Where-Object { $_.Name -match '^\d{3}_' }
if ($remaining.Count -gt 0) {
    Write-Host "`nArchivos sin renombrar ($($remaining.Count)):" -ForegroundColor Yellow
    $remaining | ForEach-Object { Write-Host "  - $($_.Name)" }
} else {
    Write-Host "`nTodos los archivos fueron renombrados correctamente." -ForegroundColor Green
}

# Contar por categoría
$allFiles = Get-ChildItem -Path $sourceDir
$carteles = $allFiles | Where-Object { $_.Name -like 'cartel-*' }
$fotos = $allFiles | Where-Object { $_.Name -like 'foto-*' }
$docs = $allFiles | Where-Object { $_.Name -like 'doc-*' }
$otros = $allFiles | Where-Object { $_.Name -notlike 'cartel-*' -and $_.Name -notlike 'foto-*' -and $_.Name -notlike 'doc-*' -and $_.Name -notlike 'escudo-*' -and $_.Name -notlike 'logo-*' }

Write-Host "`nResumen:" -ForegroundColor Green
Write-Host "  Carteles: $($carteles.Count)" -ForegroundColor Cyan
Write-Host "  Fotos: $($fotos.Count)" -ForegroundColor Cyan
Write-Host "  Documentos: $($docs.Count)" -ForegroundColor Cyan
Write-Host "  Otros: $($otros.Count)" -ForegroundColor Cyan
Write-Host "  Total: $($allFiles.Count)" -ForegroundColor Green
